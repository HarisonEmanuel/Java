public class NumerosImpares {
    public static void main(String[] args) {
        // Itera de 0 a 20
        for (int i = 0; i <= 20; i++) {
            // Verifica se o número é ímpar
            if (i % 2 != 0) {
                // Imprime números ímpares
                System.out.println(i);
            }
        }
    }
}
